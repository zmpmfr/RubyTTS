puts "What is your favorite Crayola crayon?"

answer = gets.chomp

if answer == "Red"
	puts answer.upcase.reverse
elsif answer == "Green"
	puts answer.upcase.reverse
elsif answer == "Blue"
	puts answer.upcase.reverse
elsif answer == "Yellow"
	puts answer.upcase.reverse
elsif answer == "Black"
	puts answer.upcase.reverse
elsif answer == "Purple"
	puts answer.upcase.reverse
elsif answer == "Pink"
	puts answer.upcase.reverse
elsif answer == "White"
	puts answer.upcase.reverse
else puts "That's not a crayon color!"
end
